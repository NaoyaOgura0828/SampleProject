package jp.learningdesign.javastudy;

public class Calc {

    public static void main(String[] args) {
        // 算術演算子 + - * / %
        int a = 15;
        int b = 4;
        System.out.println(a + b); // 足し算の出力
        // System.out.println("\n"); // 改行の挿入
        System.out.println(a - b); // 引き算の出力
        System.out.println(a * b); // 掛け算の出力
        System.out.println(a / b); // 割り算の出力
        System.out.println(a % b); // 余りの出力
    }
}
